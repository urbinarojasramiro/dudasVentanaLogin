package modelo;

/**
 *
 * @author Cetecom
 */
public class Usuario {
    private String user;
    private String pass;
    private String estado;

    public Usuario() {
    }

    public Usuario(String user, String pass, String estado) {
        this.user = user;
        this.pass = pass;
        this.estado = estado;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
}
